package DSL

case class Point(var x: Double, var y: Double){
  def +(other: Point): Point = Point(x + other.x, y + other.y)
  def -(other: Point): Point = Point(x - other.x, y - other.y)
  def <(other: Point): Boolean = x < other.x && y < other.y
  def >(other: Point): Boolean = x > other.x && y > other.y
  def ==(other: Point): Boolean = x == other.x && y == other.y
}
